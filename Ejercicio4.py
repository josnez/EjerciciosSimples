def dia():
    d = int(input("Digite un numero del 1 al 7\n"))
    if d==1:
    	return "Lunes"
    elif d==2:
    	return "Martes"
    elif d==3:
    	return "Miercoles"
    elif d==4:
    	return "Jueves"
    elif d==5:
    	return "Viernes"
    elif d==6:
    	return "Sabado"
    elif d==7:
    	return "Domingo"

print(dia())
